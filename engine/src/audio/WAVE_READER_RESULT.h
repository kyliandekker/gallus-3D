namespace gallus
{
	namespace audio
	{
		enum class WAVE_READER_RESULT
		{
			OK,
			ERR_FILE_NOTFOUND,
			ERR_CHUNK_NOT_FOUND,
			ERR_FILE_BAD,
			ERR_FILE_UNSUPPORTED,
			ERR_BITS_PER_SAMPLE_UNSUPPORTED,
			ERR_UNSUPPORTED,
			ERR_CHUNK_COLLECTION_NOT_INITIALIZED,
			ERR_NOT_ENOUGH_SPACE,
			ERR_NOT_ENOUGH_BUFFER_SPACE
		};

#define WAVEREADERFAILED(result) result != gallus::audio::WAVE_READER_RESULT::OK
	}
}